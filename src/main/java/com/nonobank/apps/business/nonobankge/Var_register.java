package com.nonobank.apps.business.nonobankge;

public class Var_register {
	
	private String mobile;
	private String pictureVerification;
	private String smsCode;
	private String password;
	
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getPictureVerification() {
		return pictureVerification;
	}
	public void setPictureVerification(String pictureVerification) {
		this.pictureVerification = pictureVerification;
	}
	public String getSmsCode() {
		return smsCode;
	}
	public void setSmsCode(String smsCode) {
		this.smsCode = smsCode;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
		
}
